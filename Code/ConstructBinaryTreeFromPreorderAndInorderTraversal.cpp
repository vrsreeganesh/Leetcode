TreeNode* foo(vector<int> preorder, vector<int> inorder)
{

    // returning for trivial case
    if (preorder.size() == 0 || inorder.size() == 0)   {return static_cast<TreeNode*>(nullptr);}

    // setup
    auto& rootvalue     {preorder[0]};
    auto root           {new TreeNode(preorder[0])};

    // sending it back for trivial case
    if (preorder.size() == 1 && inorder.size() == 1)    {return root;}

    // finding root-position
    auto rootposition   = std::find(inorder.begin(),
                                    inorder.end(),
                                    rootvalue);

    // creating left-subtree
    auto leftinorder   {vector<int>(inorder.begin(), rootposition)};
    auto rightinorder  {vector<int>(rootposition+1, inorder.end())};
    
    // creating subset of preorder
    auto leftpreorder   {vector<int>(preorder.begin()+1, 
                                     preorder.begin() + leftinorder.size() + 1)};
    auto rightpreorder  {vector<int>(preorder.begin() + leftinorder.size()+1,
                                     preorder.end())};
    
    // calling the function on left and right
    root->left      = foo(leftpreorder,     leftinorder);
    root->right     = foo(rightpreorder,    rightinorder);
    
    // returning the 
    return root;

}
