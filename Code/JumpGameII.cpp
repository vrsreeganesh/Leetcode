struct Timer
{
    std::chrono::time_point<std::chrono::high_resolution_clock> startpoint;
    std::chrono::time_point<std::chrono::high_resolution_clock> endpoint;
    std::chrono::duration<long long, std::nano>                 duration;
    
    // constructor
    Timer()         {startpoint = std::chrono::high_resolution_clock::now();}
    void start()    {startpoint = std::chrono::high_resolution_clock::now();}
    void stop()     {endpoint   = std::chrono::high_resolution_clock::now(); fetchtime();}
    
    void fetchtime(){
        duration = std::chrono::duration_cast<std::chrono::nanoseconds>(endpoint - startpoint);
        cout << format("{}  nanoseconds \n", duration.count());
    }
    void fetchtime(string stringarg){
        duration = std::chrono::duration_cast<std::chrono::nanoseconds>(endpoint - startpoint);
        cout << format("{} took {}  nanoseconds \n", stringarg, duration.count());
    }
    void measure(){
        auto temp = std::chrono::high_resolution_clock::now();
        auto nsduration  = std::chrono::duration_cast<std::chrono::nanoseconds>(temp - startpoint);
        auto msduration  = std::chrono::duration_cast<std::chrono::microseconds>(temp - startpoint);
        auto sduration   = std::chrono::duration_cast<std::chrono::seconds>(temp - startpoint);
        cout << format("{} nanoseconds | {} microseconds | {} seconds \n", 
            nsduration.count(), msduration.count(), sduration.count());
    }
};


// main-file ===================================================================
int main(){
    
    // input- configuration
    vector<int> nums    {2,3,0,1,4};

    // setup
    Timer timer;                                                                // setting a timer
    vector<int> minjumps(nums.size(),0);                                        // the dp table
    int leftboundary    {-1};                                                   // variable to hold the left-boundary
    int rightboundary   {-1};                                                   // variable to hold the right-boundary

    // moving from the back
    for(int i = nums.size()-2; i>=0; --i){

        // continuign if nums[i] = 0
        if (nums[i] == 0)   {
            minjumps[i] = std::numeric_limits<int>::max();                      // to prevent this from being chosen
            continue;                                                           // moving to next index
        }

        // range of values it can go from here
        leftboundary    = i+1;                                                  // the starting point of range
        rightboundary   = i+nums[i];                                            // the end point of range
        rightboundary   = rightboundary < nums.size()-1 ?                      
                          rightboundary : nums.size()-1;                        // ensuring within vector range

        // calculating smallest element in range
        auto it = std::min_element(minjumps.begin()+leftboundary, 
                                   minjumps.begin()+rightboundary+1);           // finding the minimum value in the range

        // addding min-element to the array
        if (*it == std::numeric_limits<int>::max())
            minjumps[i] = std::numeric_limits<int>::max();                      // ensuring infty logic
        else
            minjumps[i] = (1 + *it);                                            // for regular values

    }

    // printing
    cout << format("finaloutput = {}\n", minjumps[0]);
    timer.measure();

    // return
    return(0);
    
}