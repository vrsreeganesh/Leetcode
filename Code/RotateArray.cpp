struct StopWatch
{
    std::chrono::time_point<std::chrono::high_resolution_clock> startpoint;
    std::chrono::time_point<std::chrono::high_resolution_clock> endpoint;
    std::chrono::duration<long long, std::nano>                 duration;
    
    // constructor
    StopWatch()     {startpoint = std::chrono::high_resolution_clock::now();}
    void start()    {startpoint = std::chrono::high_resolution_clock::now();}
    void stop()     {endpoint   = std::chrono::high_resolution_clock::now(); fetchtime();}
    
    void fetchtime(){
        duration = std::chrono::duration_cast<std::chrono::nanoseconds>(endpoint - startpoint);
        cout << format("{}  nanoseconds \n", duration.count());
    }
    void fetchtime(string stringarg){
        duration = std::chrono::duration_cast<std::chrono::nanoseconds>(endpoint - startpoint);
        cout << format("{} took {}  nanoseconds \n", stringarg, duration.count());
    }
};


// main-file ===================================================================
int main(){
    
    // input- configuration
    vector<int> nums    {-1,-100,3,99};
    int k   {2};

    // setup
    StopWatch timer;                                        // setting up the timer
    k = k %static_cast<int>(nums.size());                   // to ensure that the value is within range

    int source          {0};
    int temp_source     {nums[source]};
    int temp            {0};
    int destination     {0};

    vector<bool> sourcelist(nums.size(), false);

    // going through nums
    for(int i = 0; i < nums.size(); ++i){

        // check if curent-source has been taken care of
        if (sourcelist[source] == true){
            source      = (source+1) % nums.size(); 
            temp_source = nums[source];
        }

        source              = source % nums.size();         // code to ensure range
        destination         = (source + k)%nums.size();     // calculating the index we'll be writing to 
        sourcelist[source]  = true;                         // updating source-list

        temp                = nums[destination];            // safe-keeping the destination value
        nums[destination]   = temp_source;                  // storing new value at destination-index

        source              = destination;                  // updating source-index
        temp_source         = temp;                         // updating source-value
    }

    // printing the output
    cout << format("nums = "); fpv(nums);                   // printing the updated array, "nums"
    timer.stop();                                           // printing the time taken 
    
    // return
    return(0);    
}
