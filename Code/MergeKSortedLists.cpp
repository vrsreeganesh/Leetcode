    auto head2  {new ListNode(2)};
    head2->next = new ListNode(6);

    vector<ListNode*> lists {head0, head1, head2};
    

    // setup
    ListNode*           prehead = new ListNode();
    ListNode*           traveller       {prehead};
    bool                runcondition    {true};
    vector<ListNode*>   travellerList; 
    int                 smallestindex   {-1};
    int                 smallestvalue   {std::numeric_limits<int>::max()};
    
    // filling up the traveller lists
    for(int i = 0; i<lists.size(); ++i) {travellerList.push_back(lists[i]);}
    
    // running the loop
    int counter {0};
    while(runcondition){
        
        // going through the values
        for(int i = 0; i < travellerList.size(); ++i){
            if (travellerList[i]        !=  nullptr && 
                travellerList[i]->val   <   smallestvalue) {
                smallestvalue = travellerList[i]->val; 
                smallestindex = i;
            }
        }
        
        // now that we have the smallest value and smallest index, we add to the prehead
        if (smallestindex == -1) {break;}
        traveller->next                 = travellerList[smallestindex];
        traveller                       = traveller->next;
        travellerList[smallestindex]    = travellerList[smallestindex]->next;
        
        // resetting
        smallestvalue = std::numeric_limits<int>::max();
        smallestindex = -1;
    }

    // printing the final-linked list
    fPrintLinkedList("final-output = ", prehead->next);

    // return
