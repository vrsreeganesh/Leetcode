struct StopWatch
{
    std::chrono::time_point<std::chrono::high_resolution_clock> startpoint;
    std::chrono::time_point<std::chrono::high_resolution_clock> endpoint;
    std::chrono::duration<long long, std::nano>                 duration;
    
    // constructor
    StopWatch()     {startpoint = std::chrono::high_resolution_clock::now();}
    void start()    {startpoint = std::chrono::high_resolution_clock::now();}
    void stop()     {endpoint   = std::chrono::high_resolution_clock::now(); fetchtime();}
    
    void fetchtime(){
        duration = std::chrono::duration_cast<std::chrono::nanoseconds>(endpoint - startpoint);
        cout << format("{}  nanoseconds \n", duration.count());
    }
    void fetchtime(string stringarg){
        duration = std::chrono::duration_cast<std::chrono::nanoseconds>(endpoint - startpoint);
        cout << format("{} took {}  nanoseconds \n", stringarg, duration.count());
    }
};


// main-file ===================================================================
int main(){
    
    // input- configuration
    vector<int> nums        {1,1};

    // setup
    int prevvalue           {nums[0]};
    int sourceindex         {1};
    int destinationindex    {1};
    int counter             {0};

    // going through it
    while(sourceindex < nums.size()){

        // moving source until different from previous value
        while(sourceindex < nums.size() && nums[sourceindex] == prevvalue)   
            ++sourceindex;

        // writing to output 
        if (destinationindex < nums.size() && sourceindex < nums.size()) {
            
            nums[destinationindex]  = nums[sourceindex];                        // copying values from source-index to destination-index
            prevvalue               = nums[sourceindex];                        // updating previous-value for next-run
            
            ++destinationindex;                                                 // updating destination index
            ++sourceindex;                                                      // updating source-index
            ++counter;
        }
    }

    // printing the final outputs
    cout << format("nums = "); fpv(nums);
    cout << format("return-value = {}\n", counter);

    // return
    return(0);
    
}
